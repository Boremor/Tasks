<?php
$array = [
    ["Иванов", "Иван", "Иванович"],
    ["Петров", "Петр", "Петрович"],
    ["Сергеев", "Сергей", "Сергеевич"]
];

echo '<p><b>Исходный массив:</b></p>';
for ($i = 0; $i < count($array); $i++) {
    for ($j = 0; $j < count($array[$i]); $j++) {
        echo "{$array[$i][$j]} | ";
    }
    echo '<br>';
}
?>